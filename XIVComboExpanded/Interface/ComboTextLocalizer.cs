    public static string LocalizeTitle(string title)
    {
        if (string.IsNullOrWhiteSpace(title))
            return title;

        if (TitleOverrides.TryGetValue(title, out var localized))
            return localized;

        return Cleanup(ApplyTitleRules(ReplaceTerms(title)));
    }

    public static string LocalizeDescription(string description)
    {
        if (string.IsNullOrWhiteSpace(description))
            return description;

        if (DescriptionOverrides.TryGetValue(description, out var localized))
            return localized;

        var result = description.Replace("\\n", "\n", StringComparison.Ordinal);
        result = ReplaceTerms(result);
        result = ApplyDescriptionRules(result);
        return Cleanup(result);
    }

    public static string LocalizeJob(string jobName)
        => string.IsNullOrWhiteSpace(jobName)
            ? string.Empty
            : Terms.TryGetValue(jobName, out var localized) ? localized : jobName;

    public static string LocalizeRole(string roleName)
        => string.IsNullOrWhiteSpace(roleName)
            ? string.Empty
            : Terms.TryGetValue(roleName, out var localized) ? localized : roleName;

    private static string ReplaceTerms(string text)
    {
        var result = text;
        foreach (var term in Terms.OrderByDescending(static pair => pair.Key.Length))
        {
            result = result.Replace(term.Key, term.Value, StringComparison.Ordinal);
        }

        return result;
    }

    private static string ApplyTitleRules(string text)
    {
        return text
            .Replace("AoE", "范围", StringComparison.Ordinal)
            .Replace("AOE", "范围", StringComparison.Ordinal)
            .Replace(" to ", "切", StringComparison.Ordinal)
            .Replace(" into ", "切", StringComparison.Ordinal)
            .Replace(" Feature", "功能", StringComparison.Ordinal)
            .Replace(" Combo", "连击", StringComparison.Ordinal)
            .Replace(" Option", "选项", StringComparison.Ordinal)
            .Replace(" Level Sync", "同步等级", StringComparison.Ordinal)
            .Replace(" Priority", "优先", StringComparison.Ordinal)
